first_name = 'kenny'

print(type(first_name))          # str
print(type(10))                  # int
print(type(3.14))                # float
print(type(1 + 1j))              # complex
print(type(True))                # bool
print(type([1, 2, 3, 4]))        # list
print(type({'name':'kenny'})) # dict
print(type((1,2)))               # tuple
print(type(zip([1,2],[3,4])))    # zip